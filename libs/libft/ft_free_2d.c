#include "libft.h"

void	ft_free_2d(char **arr)
{
	int	i;

	i = 0;
	if (arr == NULL && !arr[0])
		return ;
	while (arr[i])
	{
		ft_free(arr[i]);
		i++;
	}
	ft_free(arr);
}
